    if not errors:
        print("  PASS — all files transferred correctly.")
    else:
        df_err = pd.DataFrame(errors)
        out = HIRES_FINAL / "TRANSFER_ERRORS_LOG.csv"
        df_err.to_csv(out, index=False)
        print(f"  {len(errors)} issues. Saved: {out}")
