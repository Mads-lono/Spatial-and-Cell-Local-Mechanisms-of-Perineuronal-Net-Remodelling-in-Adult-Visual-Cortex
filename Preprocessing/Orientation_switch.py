#!/usr/bin/env python3
"""
diagnostics/mismatch_catcher.py
================================
One-time QC pipeline that verifies Fiji-generated section thumbnails against
reference thumbnails, resolves any naming or orientation mismatches, and
applies the resulting corrections to all downstream file types.

Background
----------
Free-floating brain sections are mounted without a consistent orientation.
The Fiji conversion script (00a_vsi_to_tiff.py) assigns slice IDs based on
acquisition order, but the order in which sections were picked up from the
PBS tray may differ from the order in which they were originally numbered.
This script uses SSIM-based image matching to verify that each generated
thumbnail corresponds to the correct reference thumbnail, and applies
horizontal flips where needed to standardise hemisphere orientation.

Pipeline stages
---------------
  Stage 1  — SSIM cross-match: generated vs reference thumbnails
             → MASTER_MATCHING_SUMMARY.csv
  Stage 2  — Rigorous per-channel SSIM + histogram correlation + PSNR
             → RIGOROUS_ANALYSIS_REPORT.csv  (+ diff maps for low confidence)
  Stage 3  — Recovery pass for mismatches (green-channel exhaustive search)
             → RECOVERY_REPORT.csv
  Stage 4  — FINAL_RENAME_LIST.csv from rigorous report
  Stage 5  — QC threshold split
             → RE_ANALYZE_LIST.csv, VERIFIED_LIST.csv
  Stage 6  — Morphology engine (Sobel + CLAHE) for remaining low-confidence
             → MORPHOLOGY_RECOVERY_REPORT.csv
  Stage 7  — Visual gallery for morphology results
  Stage 8  — Mirror-aware recovery (normal + fliplr comparison)
             → MIRROR_RECOVERY_REPORT.csv
  Stage 9  — Visual gallery for mirror results
  Stage 10 — Consolidate verified + morphology + mirror
             → PROJECT_GOLDEN_MASTER_LIST.csv
  Stage 11 — Visual audit gallery from golden master
  Stage 12 — Apply golden master: rename + mirror split_8bit TIFFs
             → /path/to/FINAL_PREPPED/
  Stage 13 — Apply to C1 CPN prediction PNGs (in-place)
  Stage 14 — Apply to C2 CPN prediction PNGs (in-place)
  Stage 15 — Rename counts CSVs in DATA/{animal}/counts/
  Stage 16 — Copy + mirror hiRes TIFFs → HIRES_FINAL/
  Stage 17 — Integrity audit (file exists + size check)
  Stage 18 — Safe structure swap (backup old DATA, move HIRES_FINAL to DATA)
  Stage 19 — Restore subfolders from backup into new DATA structure
  Stage 20 — Apply golden master to original multi-channel TIFFs
             → /path/to/ORIGINALS_RENAMED/
  Stage 21 — C3 symmetry audit (injection side check)
             → C3_Symmetry_Audit.csv
  Stage 22 — Mirror C3-flagged files in-place
  Stage 23 — Verification pass → FINAL_ORIENTATION_VERIFICATION.csv
  Stage 24 — Visual comparison gallery vs reference thumbnails
  Stage 25 — Manual flip of residual files identified from gallery review
  Stage 26 — Produce FINAL_TIFF_TRANSFORMATION_LOG.csv

The FINAL_TIFF_TRANSFORMATION_LOG.csv produced by Stage 26 is the input
to diagnostics/fix_c3_csv_coordinates.py, which applies the corresponding
coordinate correction to Cellpose detection CSVs.

Usage
-----
Run stages sequentially. Each stage reads the CSV output of the previous
one, so stopping between stages for manual review is expected and encouraged
(particularly after Stages 7 and 11 before committing to the file operations
in Stages 12–20).

    python diagnostics/mismatch_catcher.py

To run only specific stages, comment out the function calls at the bottom
of this file.

Requirements
------------
    pip install numpy pandas scikit-image tqdm pillow matplotlib
"""

