# STAGE 3 — Recovery pass for mismatches
# =============================================================================

def stage3_recovery():
    print("\n=== Stage 3: Recovery pass ===")
    RECOVERY_DIR.mkdir(parents=True, exist_ok=True)

    df = pd.read_csv(DETAILED_DIR / "RIGOROUS_ANALYSIS_REPORT.csv")
    mismatches = df[df["Status"].str.contains("MISMATCH")].copy()
    if mismatches.empty:
        print("  No mismatches — nothing to recover.")
        return

    recovery_data = []
    for animal, group in tqdm(mismatches.groupby("Animal"), desc="Animals"):
        ref_folder = find_ref_folder(animal)
        if not ref_folder:
            continue
        ref_pool = {}
        for f in ref_folder.glob("*.jpg"):
            if f.name.startswith("._"):
                continue
            sid = re.search(r"(s\d+)", f.name)
            if sid:
                img = load_green_300(f)
                if img is not None:
                    ref_pool[sid.group(1)] = img

        for _, row in group.iterrows():
            gen_id = row["HiRes_Filename_ID"]
            gen_files = list((GEN_BASE / animal).glob(f"*{gen_id}.jpg"))
            if not gen_files:
                continue
            gen_img = load_green_300(gen_files[0])
            if gen_img is None:
                continue

            best_score, best_id = -1, None
            for r_id, r_img in ref_pool.items():
                score = ssim(gen_img, r_img, data_range=1.0)
                if score > best_score:
                    best_score, best_id = score, r_id

            recovery_data.append({
                "Animal": animal,
                "HiRes_Slice": gen_id,
                "Original_Wrong_Match": row["Matched_Thumbnail_ID"],
                "New_Recovered_Match": best_id,
                "Recovery_Score": round(best_score, 4),
                "Note": "Corrected" if gen_id == best_id else "Still Mismatched",
            })

    df_out = pd.DataFrame(recovery_data)
    out = RECOVERY_DIR / "RECOVERY_REPORT.csv"
    df_out.to_csv(out, index=False)
    corrected = (df_out["Note"] == "Corrected").sum()
    print(f"  Corrected {corrected}/{len(mismatches)} mismatches. Saved: {out}")

