def stage8_mirror_recovery():
    print(f"\n=== Stage 8: Mirror-aware recovery (threshold {MIRROR_THRESHOLD}) ===")
    MIRROR_DIR.mkdir(parents=True, exist_ok=True)

    df = pd.read_csv(MORPH_DIR / "MORPHOLOGY_RECOVERY_REPORT.csv")
    low = df[df["Morphology_Score"] < MIRROR_THRESHOLD].copy()
    if low.empty:
        print(f"  No slices below {MIRROR_THRESHOLD}.")
        return

    mirror_results = []
    for animal, group in tqdm(low.groupby("Animal"), desc="Animals"):
        ref_folder = find_ref_folder(animal)
        if not ref_folder:
            continue
        ref_pool = {}
        for f in ref_folder.glob("*.jpg"):
            if f.name.startswith("._"):
                continue
            sid = re.search(r"(s\d+)", f.name)
            if sid:
                m = morphology_map(f)
                if m is not None:
                    ref_pool[sid.group(1)] = {
                        "normal": m, "mirrored": np.fliplr(m)
                    }

        for _, row in group.iterrows():
            gen_id = row["Original_HiRes_ID"]
            gen_files = list((GEN_BASE / animal).glob(f"*{gen_id}.jpg"))
            if not gen_files:
                continue
            gen_map = morphology_map(gen_files[0])
            if gen_map is None:
                continue

            best_score, best_id, was_mirrored = -1, None, False
            for r_id, orientations in ref_pool.items():
                for key, candidate in orientations.items():
                    score = ssim(gen_map, candidate,
                                 data_range=candidate.max() - candidate.min())
                    if score > best_score:
                        best_score = score
                        best_id = r_id
                        was_mirrored = (key == "mirrored")

            mirror_results.append({
                "Animal": animal,
                "HiRes_ID": gen_id,
                "Previous_Match": row["New_Suggested_Match"],
                "New_Match": best_id,
                "New_Score": round(best_score, 4),
                "Is_Mirrored": was_mirrored,
                "Status": "Mirror_Corrected" if was_mirrored else "Verified_Orientation",
            })

    df_out = pd.DataFrame(mirror_results)
    out = MIRROR_DIR / "MIRROR_RECOVERY_REPORT.csv"
    df_out.to_csv(out, index=False)
    print(f"  Saved: {out}")

