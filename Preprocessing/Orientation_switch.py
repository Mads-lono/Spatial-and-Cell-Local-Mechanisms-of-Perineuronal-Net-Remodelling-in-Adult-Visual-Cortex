def stage2_rigorous_analysis():
    print("\n=== Stage 2: Rigorous per-channel analysis ===")
    DETAILED_DIR.mkdir(parents=True, exist_ok=True)

    df = pd.read_csv(REPORT_DIR / "MASTER_MATCHING_SUMMARY.csv")
    results = []

    for _, row in tqdm(df.iterrows(), total=len(df)):
        animal, gen_id, ref_id = row["Animal"], row["HiRes_Filename_ID"], row["Matched_Thumbnail_ID"]

        ref_folder = find_ref_folder(animal)
        if not ref_folder:
            continue
        gen_files = list((GEN_BASE / animal).glob(f"*{gen_id}.jpg"))
        ref_files = list(ref_folder.glob(f"*{ref_id}.jpg"))
        if not gen_files or not ref_files:
            continue

        img_gen = io.imread(gen_files[0])
        img_ref = io.imread(ref_files[0])

        h, w = img_ref.shape[:2]
        img_gen_r = transform.resize(
            img_gen, (h, w), anti_aliasing=True, preserve_range=True
        ).astype(np.uint8)

        s_r = ssim(img_ref[:, :, 0], img_gen_r[:, :, 0], data_range=255)
        s_g = ssim(img_ref[:, :, 1], img_gen_r[:, :, 1], data_range=255)
        s_b = ssim(img_ref[:, :, 2], img_gen_r[:, :, 2], data_range=255)

        from skimage import color as skcolor
        h1 = np.histogram(skcolor.rgb2gray(img_ref),    bins=256, range=(0, 255))[0]
        h2 = np.histogram(skcolor.rgb2gray(img_gen_r),  bins=256, range=(0, 255))[0]
        hist_corr = np.corrcoef(h1, h2)[0, 1]

        try:
            p_val = psnr(img_ref, img_gen_r, data_range=255)
        except Exception:
            p_val = 0

        global_conf = (s_r + s_g + hist_corr) / 3
        full_row = row.to_dict()
        full_row.update({
            "SSIM_Red": round(s_r, 4), "SSIM_Green": round(s_g, 4),
            "SSIM_Blue": round(s_b, 4), "Hist_Corr": round(hist_corr, 4),
            "PSNR": round(p_val, 2), "Rigorous_Confidence": round(global_conf, 4),
        })
        results.append(full_row)

        if global_conf < 0.7:
            fig, ax = plt.subplots(1, 3, figsize=(18, 6))
            ax[0].imshow(img_ref);   ax[0].set_title(f"Ref: {ref_id}")
            ax[1].imshow(img_gen);   ax[1].set_title(f"Gen: {gen_id}")
            diff = np.abs(
                img_ref.astype(float) -
                transform.resize(img_gen, img_ref.shape[:2], preserve_range=True)
            )
            ax[2].imshow(np.mean(diff, axis=2), cmap="hot")
            ax[2].set_title("Difference")
            for a in ax:
                a.axis("off")
            plt.suptitle(f"{animal} | Conf: {global_conf:.2f}")
            plt.savefig(DETAILED_DIR / f"diff_{animal}_{gen_id}.png")
            plt.close()

    df_out = pd.DataFrame(results)
    out = DETAILED_DIR / "RIGOROUS_ANALYSIS_REPORT.csv"
    df_out.to_csv(out, index=False)
    print(f"  Saved: {out}")
    return df_out

