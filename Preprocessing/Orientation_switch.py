def stage1_ssim_crossmatch():
    print("\n=== Stage 1: SSIM cross-match ===")
    REPORT_DIR.mkdir(parents=True, exist_ok=True)

    animal_folders = [f for f in GEN_BASE.iterdir() if f.is_dir()]
    all_results = []

    for gen_folder in tqdm(animal_folders, desc="Animals"):
        animal_id = gen_folder.name
        ref_folder = find_ref_folder(animal_id)
        if not ref_folder:
            print(f"  WARNING: {animal_id} not found in REF_BASE")
            continue

        ref_data = {}
        for f in ref_folder.glob("*.jpg"):
            if f.name.startswith("._"):
                continue
            sid = extract_slice_id(f.name)
            if sid:
                img = load_gray_300(f)
                if img is not None:
                    ref_data[sid] = img

        gen_data = {}
        for f in gen_folder.glob("*.jpg"):
            if f.name.startswith("._"):
                continue
            sid = extract_slice_id(f.name)
            if sid:
                img = load_gray_300(f)
                if img is not None:
                    gen_data[sid] = img

        if not ref_data or not gen_data:
            print(f"  Skipping {animal_id}: missing JPGs")
            continue

        for g_id, g_img in gen_data.items():
            best_score, best_match = -1, None
            for r_id, r_img in ref_data.items():
                score = ssim(g_img, r_img, data_range=1.0)
                if score > best_score:
                    best_score, best_match = score, r_id

            status = "OK" if g_id == best_match else "MISMATCH"
            if best_score < 0.85:
                status += " (LOW CONFIDENCE)"
            all_results.append({
                "Animal": animal_id,
                "HiRes_Filename_ID": g_id,
                "Matched_Thumbnail_ID": best_match,
                "Similarity_Score": round(best_score, 4),
                "Status": status,
            })

    df = pd.DataFrame(all_results)
    out = REPORT_DIR / "MASTER_MATCHING_SUMMARY.csv"
    df.to_csv(out, index=False)
    print(f"  Saved: {out}  ({len(df)} rows, "
          f"{df['Status'].str.contains('MISMATCH').sum()} mismatches)")
    return df

