def stage26_transformation_log():
    print("\n=== Stage 26: FINAL_TIFF_TRANSFORMATION_LOG.csv ===")

    df_master = pd.read_csv(REPORT_DIR / "PROJECT_GOLDEN_MASTER_LIST.csv")
    df_log = df_master[["Animal", "HiRes_ID", "Correct_ID", "Needs_Mirroring"]].copy()
    df_log.columns = ["Animal", "Original_ID", "Final_ID", "Flipped_in_Step1"]
    df_log["Final_Filename"] = df_log.apply(
        lambda r: f"{r['Animal']}_{r['Final_ID']}.tiff", axis=1
    )

    audit_csv = ORIGINALS_RENAMED / "C3_Symmetry_Audit.csv"
    if audit_csv.exists():
        df_audit = pd.read_csv(audit_csv)
        flipped_in_audit = df_audit[df_audit["Needs_Flip_Check"] == "YES"]["File"].tolist()
    else:
        print("  Warning: C3_Symmetry_Audit.csv not found.")
        flipped_in_audit = []

    df_log["Flipped_in_C3_Audit"]    = df_log["Final_Filename"].isin(flipped_in_audit)
    df_log["Flipped_in_Manual_Final"]= df_log["Final_Filename"].isin(MANUAL_FLIP_LIST)

    def net_change(row):
        n_flips = sum([
            row["Flipped_in_Step1"],
            row["Flipped_in_C3_Audit"],
            row["Flipped_in_Manual_Final"],
        ])
        return "Mirrored" if n_flips % 2 != 0 else "Original Orientation"

    df_log["Net_Orientation_Change"] = df_log.apply(net_change, axis=1)

    out = ORIGINALS_RENAMED / "FINAL_TIFF_TRANSFORMATION_LOG.csv"
    df_log.to_csv(out, index=False)
    mirrored = (df_log["Net_Orientation_Change"] == "Mirrored").sum()
    print(f"  {len(df_log)} entries  |  {mirrored} net-mirrored")
    print(f"  Saved: {out}")
    print(df_log.head(10).to_string(index=False))

