def extract_slice_id(filename: str):
    m = re.search(r"(s\d+)", filename)
    return m.group(1) if m else None


def load_gray_300(path) -> np.ndarray:
    """Load image, resize to 300×300, return grayscale float array."""
    try:
        img = io.imread(path)
        img_small = transform.resize(img, (300, 300), anti_aliasing=True)
        return np.mean(img_small, axis=2) if img_small.ndim == 3 else img_small
    except Exception as e:
        print(f"  Error loading {path}: {e}")
        return None


def load_green_300(path) -> np.ndarray:
    """Load image, resize to 300×300, return green channel (most structural)."""
    try:
        img = io.imread(path)
        img_small = transform.resize(img, (300, 300), anti_aliasing=True)
        return img_small[:, :, 1] if img_small.ndim == 3 else img_small
    except Exception:
        return None


def morphology_map(path) -> np.ndarray:
    """Sobel edge map after CLAHE normalisation — orientation/colour invariant."""
    try:
        img = io.imread(path)
        img = transform.resize(img, (400, 400), anti_aliasing=True)
        gray = img[:, :, 1] if img.ndim == 3 else img
        gray = exposure.equalize_adapthist(gray)
        return filters.sobel(gray)
    except Exception:
        return None


def find_ref_folder(animal: str) -> Path | None:
    matches = [m for m in REF_BASE.rglob(animal) if m.is_dir()]
    return matches[0] if matches else None

